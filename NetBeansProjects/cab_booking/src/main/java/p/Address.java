package p;
public class Address {
    int blockNo;
    String street;
    String city;
    String state;
    String country;
    int pincode;
    public Address(int blockNo, String street, String city, String state, String country, int pincode){
        this.blockNo=blockNo;
        this.street=street;
        this.city=city;
        this.state=state;
        this.country=country;
        this.pincode=pincode;
    }
}
